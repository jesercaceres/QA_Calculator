public class Calculadora {
    public int somar(int num1, int num2) {

        return num1 + num2;
    }

    public int subtrair(int num1, int num2) {
        return num1 - num2;
    }

    public double multiplicar(double num1, double num2) {
        return num1 * num2;
    }

    public double dividir(double num1, double num2) {

        return num1 / num2;
    }
}